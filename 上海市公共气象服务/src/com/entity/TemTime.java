package com.entity;

/**
 * @author 分裂状态。
 *
 */
public class TemTime {

	private String temTimeMin;
	private String temTimeMax;
	private String temYearMax;
	private String temYearMin;
	
	public String getTemTimeMin() {
		return temTimeMin;
	}
	public void setTemTimeMin(String temTimeMin) {
		this.temTimeMin = temTimeMin;
	}
	public String getTemTimeMax() {
		return temTimeMax;
	}
	public void setTemTimeMax(String temTimeMax) {
		this.temTimeMax = temTimeMax;
	}
	public String getTemYearMax() {
		return temYearMax;
	}
	public void setTemYearMax(String temYearMax) {
		this.temYearMax = temYearMax;
	}
	public String getTemYearMin() {
		return temYearMin;
	}
	public void setTemYearMin(String temYearMin) {
		this.temYearMin = temYearMin;
	}
}
